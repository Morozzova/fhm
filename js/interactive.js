 var hov1 = document.querySelector('#scheme_item_01');
        $('#scheme_01').hover(
            function() {
                $(hov1).addClass('scheme-dark')
            },
            function() {
                $(hov1).removeClass('scheme-dark')
            }
        );


        var hov1item = document.querySelector('#scheme_01');
        $('#scheme_item_01').hover(
            function() {
                $(hov1item).addClass('number_border')
            },
            function() {
                $(hov1item).removeClass('number_border')
            }
        );
        var openItem1 = document.querySelector('#scheme_01');

        openItem1.addEventListener('click', function(evt) {
            evt.preventDefault();

            var deleteClass = document.querySelector('.scheme-dark-show');
            var deleteNumber = document.querySelector('.number_border_adaptive');
            deleteClass.classList.remove('scheme-dark-show');
            deleteNumber.classList.remove('number_border_adaptive');

            hov1.classList.add('scheme-dark-show');
            openItem1.classList.add('number_border_adaptive');
        });


